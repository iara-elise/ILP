# Entrada de dados pelo usuário.

valor_01 = int(input("Valor 01: "))
valor_02 = int(input("Valor 02: "))

# A condição verifica se valor_01 é maior que valor_2.

# Se valor_01 for maior que valor_02, o programa exibirá uma mensagem informando que valor_01 é maior que valor_02.
# Se não, o programa exibirá uma mensagem informando que valor_02 é maior que valor_01.

if valor_01 > valor_02:
    print(f"{valor_01} é maior que {valor_02}")

else:
    print(f"\n{valor_02} é maior que {valor_01}")

